package org.wj.model;

public class DemoInfo {
    private String id;

    private String name;

    private Integer info1;

    private String info2;

    private String infon;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getInfo1() {
        return info1;
    }

    public void setInfo1(Integer info1) {
        this.info1 = info1;
    }

    public String getInfo2() {
        return info2;
    }

    public void setInfo2(String info2) {
        this.info2 = info2;
    }

    public String getInfon() {
        return infon;
    }

    public void setInfon(String infon) {
        this.infon = infon;
    }
}